#예제 60번 : 다음 리스트의 평균을 출력하라.

nums = [1, 2, 3, 4, 5]        #nums 는 리스트를 의미한다.

average = sum(nums) / len(nums)   # 변수 average = nums 리스트 원소의 합 / nums 리스트 원소의 갯수 => nums 리스트의 원소들의 평균

print(average)      # average 값을 출력한다.

# 평균 = 원소의 합 / 원소의 갯수 로 나타낼 수 있다.
# sum() 명령어와 len() 명령어를 사용했다. len() -> 리스트의 길이를 나타내는 함수


