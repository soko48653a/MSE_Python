#예제 80번 : 1 부터 99까지의 정수 중 짝수만 저장된 튜플을 생성하라.

data = tuple(range(2, 100, 2))  #튜플 = tuple
                                #range(A,B,C) => A-1 부터 B 까지 C 만큼 차이로 출력  (B는 포함하지 않는다.)
                                #range(1,100,5) => 0 부터 100 까지 5만큼의 차이로 출력 ex) 5,10,15,20,,,,,,95

print(data)         #data라는 명칭의 튜플을 출력한다.


